@Component({
  selector: 'app-root',
  templateUrl: './app.component.html',
  styleUrls: ['./app.component.css'],
})
export class AppComponent {
  constructor(
    private gtmService: GoogleTagManagerService,
    router: Router,
    iconRegistry: MatIconRegistry,
    sanitizer: DomSanitizer
  ) {
   

    router.events.forEach((item) => {
      if (item instanceof NavigationEnd) {
        const gtmTag = {
          event: 'page',
          pageName: item.url,
        };

        gtmService.pushTag(gtmTag);
      }
    });

    iconRegistry
      .addSvgIconLiteral('back', sanitizer.bypassSecurityTrustHtml(BACK))
      .addSvgIconLiteral('check', sanitizer.bypassSecurityTrustHtml(CHECK))
      .addSvgIconLiteral('one', sanitizer.bypassSecurityTrustHtml(ONE))
      .addSvgIconLiteral('two', sanitizer.bypassSecurityTrustHtml(TWO))
      .addSvgIconLiteral('three', sanitizer.bypassSecurityTrustHtml(THREE))
      .addSvgIconLiteral(
        'arrow_right',
        sanitizer.bypassSecurityTrustHtml(ARROW)
      )
      .addSvgIconLiteral('menu', sanitizer.bypassSecurityTrustHtml(MENU));
  }
}
